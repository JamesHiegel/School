	/**
	 * The getImage method returns a colored BufferedImage produced by the specified
	 * integer array. Example: getImage(ImageShapes.Diamond); // returns a colored
	 * diamond BufferedImage
	 */
	public BufferedImage getImage(int[][] data) {
		BufferedImage image = new BufferedImage(IMGSIZEX, IMGSIZEY, BufferedImage.TYPE_INT_RGB);
		for (int x = 0; x < IMGSIZEY; x++) {
			for (int y = 0; y < IMGSIZEX; y++) {
				int pixelColor = data[y][x];
				if (pixelColor == 0) {
					pixelColor = Color.WHITE.getRGB();
				} else if (pixelColor == 1) {
					pixelColor = Color.RED.getRGB();
				} else {
					pixelColor = Color.YELLOW.getRGB();
				}
				image.setRGB(x, IMGSIZEX - 1 - y, pixelColor);
			}
		}
		return image;
	}
